public class JavaApplication25 {
public static void main(String[] args) {
       int s = 3;
       //increment
       System.out.println("Nilai awal s: " + s);

        System.out.println("s++ = " + s++); 
        System.out.println("Setelah s++ , s = " + s);

        System.out.println("++s = " + ++s); 
        System.out.println("Setelah ++s , s = " + s);
        
        int w = 4;
        //decrement
         System.out.println("Nilai awal w: " + w);

        System.out.println("w-- = " + w--); 
        System.out.println("Setelah w-- , w = " + w);

        System.out.println("--w = " + --w); 
        System.out.println("Setelah --w , w = " + w);
        
        
    }
    
}
