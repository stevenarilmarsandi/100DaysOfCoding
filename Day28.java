public class NewClass1{
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
      
        // Operator ==
        System.out.println("a == a: " + (a == a)); // true
        System.out.println("a == b: " + (a == b)); // false

        // Operator !=
        System.out.println("a != a: " + (a != a)); // false
        System.out.println("a != b: " + (a != b)); // true
    }
}

    
